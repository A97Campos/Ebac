function abrirFaq1(){
    const faq1 = document.getElementById("faq1");
    const img = document.getElementById("myImage");

    if (faq1.style.display == "block"){
        faq1.style.display = "none";       
        img.setAttribute('src', 'src/images/icones/icons8-soma-30.png');
    } else {
        faq1.style.display = "block";
        img.setAttribute('src', 'src/images/icones/icons8-subtração-30.png');
    }
}

